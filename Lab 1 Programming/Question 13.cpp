#include <iostream>
using namespace std;

/*Left shift(<<) is used to shift bits to the left. */
//Example: int x = 5;
//         cout << (x << 1);

/*Right shift(>>) is used to shift bits to the right.*/
//Example: int x = 20;
//         cout << (x >> 1);

int main() {
	int a = 5;
	int b = 20;;
	
	
	cout << " a << 1 =" << (a << 1) << endl;
	cout << " b >> 1 =" << (b >> 1) << endl;
	
	
	
	return 0;
}
