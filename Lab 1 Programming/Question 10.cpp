#include <iostream>
using namespace std;

/* Increment operator (++) is used to increase a variable's by 1. */
//Example: int x = 5;
//         x++;

/*Decrement operator(--) is used to decrease a variable's value by 1.*/
//Example:int y = 5;
//        y--;


int main() {
	int x = 10;
	int y = 8;
	
	x++; // Increment
	y--; // Decrement
	
	cout << "Value of x after increment: " << x << endl;
	cout << "Value of y after decrement: " << y << endl;
	
	
	return 0;
}
